<template>
  <button
    @click="toggleMode"
    class="p-2 rounded-full bg-gray-200 dark:bg-gray-700 transition-colors duration-200 hover:bg-gray-300 dark:hover:bg-gray-600 focus:outline-none"
    aria-label="تبديل وضع الليل والنهار"
  >
    <!-- أيقونة الشمس (وضع النهار) -->
    <svg
      v-if="!is_dark_mode"
      xmlns="http://www.w3.org/2000/svg"
      class="w-6 h-6 text-yellow-500"
      fill="none"
      viewBox="0 0 24 24"
      stroke="currentColor"
    >
      <path
        stroke-linecap="round"
        stroke-linejoin="round"
        stroke-width="2"
        d="M12 3v1m0 16v1m9-9h-1M4 12H3m15.364 6.364l-.707-.707M6.343 6.343l-.707-.707m12.728 0l-.707.707M6.343 17.657l-.707.707M16 12a4 4 0 11-8 0 4 4 0 018 0z"
      />
    </svg>

    <!-- أيقونة القمر (وضع الليل) -->
    <svg
      v-else
      xmlns="http://www.w3.org/2000/svg"
      class="w-6 h-6 text-blue-300"
      fill="none"
      viewBox="0 0 24 24"
      stroke="currentColor"
    >
      <path
        stroke-linecap="round"
        stroke-linejoin="round"
        stroke-width="2"
        d="M20.354 15.354A9 9 0 018.646 3.646 9.003 9.003 0 0012 21a9.003 9.003 0 008.354-5.646z"
      />
    </svg>
  </button>
</template>

<script setup>
import { ref , watch, onMounted } from 'vue';

const props = defineProps({
  modelValue: {
    type: Boolean,
    default: false
  }
});

const emit = defineEmits(['update:modelValue', 'change']);

const is_dark_mode = ref(props.modelValue);

// مراقبة التغييرات من الأب
watch(() => props.modelValue, (newVal) => {
  is_dark_mode.value = newVal;
});

const toggleMode = () => {
  is_dark_mode.value = !is_dark_mode.value;
  emit('update:modelValue', is_dark_mode.value);
  emit('change', is_dark_mode.value);
};
</script>